public class SendMessageQueryProcessor extends SendMessageProcessor implements NettyRequestProcessor {

    private List<ConsumeMessageHook> consumeMessageHookList;

    public SendMessageQueryProcessor(final BrokerController brokerController) {
        super(brokerController);
    }

    @Override
    public RemotingCommand processRequest(ChannelHandlerContext ctx,
                                          RemotingCommand request) throws RemotingCommandException {
        SendMessageContext mqtraceContext;

        SendMessageQueryRequestHeader requestQueryHeaderV2 =
                (SendMessageQueryRequestHeader) request
                        .decodeCommandCustomHeader(SendMessageQueryRequestHeader.class);
        mqtraceContext = buildMsgContext(ctx, requestQueryHeaderV2);
        RemotingCommand response = this.sendMessage(ctx, request, mqtraceContext, requestQueryHeaderV2);
        requestQueryHeaderV2.getIndex();
        return response;
    }
}
